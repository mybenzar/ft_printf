#include "libft.h"

size_t	ft_lstsize(t_list *lst)
{
	t_list	*tmp;
	size_t	size;

	size = 0;
	if (!lst)
		return (0);
	tmp = lst;
	while (tmp)
	{
		tmp = tmp->next;
		size++;
	}
	return (size);
}
