#include "libft.h"

t_list	*ft_lstat(t_list *lst, int at)
{
	t_list *tmp;

	if (!lst)
		return (NULL);
	tmp = lst;
	while (tmp && at--)
		tmp = tmp->next;
	if (!tmp)
		return (NULL);
	return (tmp);
}
